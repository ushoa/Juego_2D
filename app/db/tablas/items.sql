CREATE TABLE IF NOT EXISTS `ITEMS` (
  `ID` INT(10) NOT NULL,
  `NOMBRE`  TEXT(50) NOT NULL,
  `TIPO`  TEXT(50),
  `CAT`  TEXT(50),
  `MANOS`  TEXT(50),
  `HPMAX` INT(3),
  `ENEMAX` INT(3),
  `HP` INT(3),
  `ENE` INT(3),
  `DMG` INT(3),
  `ARM` INT(3),
  `BLO` INT(3),
  `STR` TINYINT(3),
  `DEF` TINYINT(3),
  `CON` TINYINT(3),
  `DEX` TINYINT(3),
  `ORO` MEDIUMINT(10),
  `LVL` TINYINT(3),
  `CLASE`  TEXT(1),
  `SPRITE`  TEXT(50),
  PRIMARY KEY (`ID`)
)